function buttonclick(seite){
    switch(seite) {
        case "basket":
            window.location.href = "basket-random.html";
            break;
        case "football":
            window.location.href = "football-random.html";
            break;
        case "gun":
            window.location.href = "gun-mayhem.html";
            break;
        case "yt":
            window.location.href = "youtube.html";
            break;
        case "volley":
            window.location.href = "volley-random.html";
            break;
        case "roof":
            window.location.href = "rooftop-snipers.html";
            break;
        case "soccer":
            window.location.href = "football-random.html"
    }
    
}